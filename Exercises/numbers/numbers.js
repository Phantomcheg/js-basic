// 4. use `Math.random` to generate random value from `0` to `100`.

function randomNumber(min, max) {
  return min + Math.floor(Math.random() * (max - min + 1));
}

const roundedRandomDecimal = Math.round(randomNumber(1, 100));
console.log(roundedRandomDecimal);

// # Additional
// 1. Make function which will take a array with numbers as a parameter and
// calculate average number and round to hundredths.
// 2. Generate array to pass to function above with values, and length generated by Math.random.

function averageNumberFromArray(array) {
  let summary = 0;
  array.forEach((arrayItem) => {
    summary += parseInt(arrayItem, 16);
  });
  return (summary/array.length).toFixed(3);
}

const arrayForCalc = [];
for (let i = 0; i <= randomNumber(0, 10); i++) {
  let hexNumber = randomNumber(100, 200).toString(16);
  arrayForCalc.push(hexNumber);
}

console.log(averageNumberFromArray(arrayForCalc));
