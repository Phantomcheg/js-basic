// 2. Implement function `extractNumbers` which will leave only numbers in passed string.

function extractNumbers (text) {
  const checkInt = parseFloat(text);
  if (isNaN(checkInt)) {
    return null;
  }
  if (typeof checkInt === 'number') {
    return true;
  }
}

const valueExample = '23test';
console.log(extractNumbers(valueExample));

